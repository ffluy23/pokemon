// 타입 상성 배수 반환
// 약점 → ×1.8 / 저항 → ×0.8 / 무효 → ×0 / 통상 → ×1
export function getTypeMultiplier(moveType, defenderType) {
const raw = TYPE_CHART[moveType]?.[defenderType]

if (raw === undefined) return 1    // 통상
if (raw === 2)         return 1.8  // 약점
if (raw === 0.5)       return 0.8  // 저항
if (raw === 0)         return 0    // 무효
return 1
}

// 자속보정 반환
export function getStab(moveType, pokemonType) {
return moveType === pokemonType ? 1.3 : 1
}
